public class Calculator {
    public Calculator() {
    }

    public int adding(int number1, int number2) {
        return (number1 + number2);
    }

    public int subtracting(int number1, int number2) {
        return (number1 - number2);
    }

    public int muliply(int number1, int number2) {
        return (number1 * number2);
    }
}









